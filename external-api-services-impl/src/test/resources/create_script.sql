CREATE TABLE AIRPORT (
  ID        INTEGER GENERATED BY DEFAULT AS IDENTITY,
  NAME      VARCHAR(100),
  CITY      VARCHAR(100),
  COUNTRY   VARCHAR(45),
  IATA      VARCHAR(3),
  ICAO      VARCHAR(4),
  TIMEZONE  VARCHAR(50),
  LONGITUDE DOUBLE,
  LATITUDE  DOUBLE,
  PRIMARY KEY (ID)
);

-- -----------------------------------------------------
CREATE TABLE AIRLINE (
  ID      INTEGER GENERATED BY DEFAULT AS IDENTITY,
  NAME    VARCHAR(50),
  IATA    VARCHAR(3),
  ICAO    VARCHAR(4),
  COUNTRY VARCHAR(100),
  PRIMARY KEY (ID)
);
-- -----------------------------------------------------
CREATE TABLE SEAT_CLASS (
  TYPE        VARCHAR(2),
  NAME        VARCHAR(50),
  DESCRIPTION VARCHAR(200),
  PRIMARY KEY (TYPE)
);
-- -----------------------------------------------------
CREATE TABLE PLANE (
  ID           INTEGER GENERATED BY DEFAULT AS IDENTITY,
  TYPE         VARCHAR(20),
  MANIFACTURER VARCHAR(40),
  TOTAL_SEATS  INT,
  PRIMARY KEY (ID)
);
-- -----------------------------------------------------
CREATE TABLE PLANE_SEATS (
  PLAINE_ID  INTEGER GENERATED BY DEFAULT AS IDENTITY,
  SEAT_TYPE  VARCHAR(2),
  NUMBER     INT,
  START_ROW  INT,
  END_ROW    INT,
  ROW_LENGTH INT,
  PRIMARY KEY (PLAINE_ID, SEAT_TYPE)
);

-- -----------------------------------------------------
CREATE TABLE FLIGHT_ROUTE (
  ID                   INTEGER GENERATED BY DEFAULT AS IDENTITY,
  NAME                 VARCHAR(100),
  DEPARTURE_AIRPORT_ID INT,
  ARRIVAL_AIRPORT_ID   INT,
  AIRLINE_ID           INT,
  PLANE_ID             INT,
  START_DATE           DATETIME,
  END_DATE             DATETIME,
  DEPARTURE_TIME       TIME,
  ARRIVAL_TIME         TIME,
  DAILY_SCHEDULE       INT,
  PRIMARY KEY (ID)
);

-- -----------------------------------------------------
CREATE TABLE FLIGHT (
  ID              INTEGER GENERATED BY DEFAULT AS IDENTITY,
  FLIGHT_ROUTE_ID INT,
  DATE            DATE,
  SEATS_AVAILABLE INT,
  PRIMARY KEY (ID)
);
-- -----------------------------------------------------
CREATE TABLE AIRPORT_CONNECTION (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  DEPARTURE_AIRPORT_ID INT,
  ARRIVAL_AIRPORT_ID INT,
  STOPS INT,
PRIMARY KEY (ID));
-- -----------------------------------------------------
CREATE TABLE CONNECTION_ROUTES (
  AIRPORT_CONNECTION_ID INTEGER GENERATED BY DEFAULT AS IDENTITY,
  FLIGHT_ROUTE_ID INT,
  POSITION INT,
PRIMARY KEY (AIRPORT_CONNECTION_ID, FLIGHT_ROUTE_ID));